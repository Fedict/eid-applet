package be.fedict.eid.applet.service.impl.tlv;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;


/**
 * Marks that a field will be converted during TLV parsing.
 * 
 * @author Frank Cornelis
 * 
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.FIELD)
@Documented
public @interface ConvertData {
	/**
	 * The data convertor class to use for conversion.
	 * 
	 * @return
	 */
	Class<? extends DataConvertor<?>> value();
}
